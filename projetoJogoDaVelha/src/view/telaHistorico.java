	public void preencherTabela(){
		this.Partidas = pDao.readPartidas();
		DefaultTableModel dtmPartidas = (DefaultTableModel) tabelaPartidas.getModel();
		dtmPartidas.setNumRows(0);
		if (jComboBoxFiltrar.getSelectedItem().equals("Todas")){
			for (int i = 0; i < Partidas.size(); i++) {
			dtmPartidas.addRow(new Object[]{
				Partidas.get(i).getId_partida(),
				Partidas.get(i).getJogador1(),
				Partidas.get(i).getJogador2(),
				Partidas.get(i).getVencedor(),
				Partidas.get(i).getData(),
				Partidas.get(i).getHora(),
				});
			}
		}
		else{
			for (int i = 0; i < Partidas.size(); i++) {
				if(jComboBoxFiltrar.getSelectedItem().equals(Partidas.get(i).getJogador1())||jComboBoxFiltrar.getSelectedItem().equals(Partidas.get(i).getJogador2())){
					dtmPartidas.addRow(new Object[]{
					Partidas.get(i).getId_partida(),
					Partidas.get(i).getJogador1(),
					Partidas.get(i).getJogador2(),
					Partidas.get(i).getVencedor(),
					Partidas.get(i).getData(),
					Partidas.get(i).getHora(),
				});
				}
			}
		}
	}
	public void preencheComboBox(){
		jogadorDAO jDao = new jogadorDAO();
		for(jogador j: jDao.readJogadores()){
			jComboBoxFiltrar.addItem(j.getNome());
		}
	}

